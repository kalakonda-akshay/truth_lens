"use client";

import Link from "next/link";
import { useParams, useSearchParams } from "next/navigation";
import { useEffect, useState } from "react";
import { AlertTriangle, ArrowLeft, CheckCircle2, Download, Eye, ScanSearch, ShieldAlert, Sparkles } from "lucide-react";
import { API_URL, fetchReport, normalizeReport, type AnalysisReport } from "@/lib/api";
import { MetricCard } from "@/components/MetricCard";

function riskTone(risk: string): "green" | "amber" | "red" {
  if (risk === "High" || risk === "Critical") return "red";
  if (risk === "Medium") return "amber";
  return "green";
}

const riskClasses = {
  green: "text-cyber-green",
  amber: "text-cyber-amber",
  red: "text-cyber-red",
};

function frameSource(frameUrl: string) {
  if (frameUrl.startsWith("data:") || frameUrl.startsWith("http")) return frameUrl;
  return `${API_URL}${frameUrl}`;
}

async function downloadPdf(report: AnalysisReport) {
  const { jsPDF } = await import("jspdf");
  const doc = new jsPDF();
  const lines = [
    "TruthLens Synthetic Media Verification Report",
    "Generated by TruthLens AI Engine",
    "TEAM TRUTHLENS",
    "See Truth. Stop Deception.",
    "",
    `Report ID: ${report.id}`,
    `Analysis Date: ${new Date(report.uploaded_at).toLocaleString()}`,
    "Version: 2.0.0",
    "Analyzed By: TruthLens AI Engine",
    "",
    `File: ${report.filename}`,
    `Media Type: ${report.media_type}`,
    `Decision: ${report.verdict}`,
    `Authenticity Score: ${report.scores.authenticity_score}%`,
    `AI/Synthetic Probability: ${report.scores.deepfake_probability}%`,
    `Risk Level: ${report.scores.risk_level}`,
    `Threat Score: ${report.scores.threat_score}%`,
    `Confidence: ${report.scores.confidence_score}%`,
    "",
    "Analysis Summary",
    report.analysis_summary,
    "",
    "Metadata",
    `File size: ${report.metadata.file_size_mb} MB`,
    `Created/modified: ${new Date(report.metadata.creation_date).toLocaleString()}`,
    `Codec: ${report.metadata.codec}`,
    `Camera: ${report.metadata.camera_information}`,
    `Editing software: ${report.metadata.editing_software}`,
    "",
    "Reasons for Decision",
    ...report.reasons_for_decision.map((reason) => `- ${reason}`),
    "",
    "Evidence",
    ...report.evidence.flatMap((item) => [`${item.label} [${item.severity}]`, item.detail, ""]),
    "",
    "Key Findings",
    ...report.key_findings.map((finding) => `- ${finding}`),
    "",
    "Conclusion",
    report.conclusion,
    "",
    "Recommendations",
    ...report.recommendations.map((recommendation) => `- ${recommendation}`),
    "",
    "Disclaimer",
    "This report has been generated using automated forensic analysis techniques. Results are probabilistic assessments and should be considered advisory in nature. TruthLens AI does not guarantee absolute authenticity or inauthenticity. Additional human verification is recommended for critical decisions.",
    "",
    "Generated by TruthLens AI Engine | TEAM TRUTHLENS | See Truth. Stop Deception.",
    report.awareness_message,
  ];

  let y = 18;
  doc.setFont("helvetica", "bold");
  doc.setFontSize(16);
  doc.text(lines[0], 14, y);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  y += 10;

  lines.slice(2).forEach((line) => {
    const wrapped = doc.splitTextToSize(line, 180);
    wrapped.forEach((wrappedLine: string) => {
      if (y > 280) {
        doc.addPage();
        y = 18;
      }
      doc.text(wrappedLine, 14, y);
      y += 6;
    });
  });

  doc.save(`truthlens-${report.id}.pdf`);
}

export default function ResultsPage() {
  const params = useParams<{ id: string }>();
  const searchParams = useSearchParams();
  const id = params.id;
  const judgeMode = searchParams.get("judge") === "1";
  const [report, setReport] = useState<AnalysisReport | null>(null);
  const [error, setError] = useState("");

  useEffect(() => {
    const stored = sessionStorage.getItem(`truthlens:report:${id}`);
    if (stored) {
      setReport(normalizeReport(JSON.parse(stored)));
      return;
    }

    fetchReport(id)
      .then((nextReport) => {
        sessionStorage.setItem(`truthlens:report:${nextReport.id}`, JSON.stringify(nextReport));
        setReport(nextReport);
      })
      .catch(() => setError("Report not found in this browser session. Run a new TruthLens analysis."));
  }, [id]);

  if (error) {
    return (
      <main className="cyber-grid flex min-h-screen items-center justify-center px-6">
        <div className="glass max-w-xl rounded-[2rem] p-8 text-center">
          <h1 className="text-3xl font-black text-white">Report unavailable</h1>
          <p className="mt-4 text-slate-300">{error}</p>
          <Link href="/" className="mt-6 inline-flex rounded-2xl bg-cyber-cyan px-6 py-3 font-black text-slate-950">
            Run new analysis
          </Link>
        </div>
      </main>
    );
  }

  if (!report) {
    return (
      <main className="cyber-grid flex min-h-screen items-center justify-center px-6">
        <div className="glass rounded-[2rem] p-8 text-center">
          <p className="text-sm font-semibold uppercase tracking-[0.35em] text-cyber-cyan">Loading report</p>
          <h1 className="mt-3 text-3xl font-black text-white">Preparing TruthLens evidence...</h1>
        </div>
      </main>
    );
  }

  const tone = riskTone(report.scores.risk_level);

  return (
    <main className="cyber-grid min-h-screen px-6 py-8">
      <div className="mx-auto max-w-7xl">
        <section className="mb-6 overflow-hidden rounded-[2rem] border border-cyber-cyan/25 bg-slate-950 shadow-glow">
          <div className="grid gap-6 bg-gradient-to-r from-slate-950 via-cyan-950/40 to-slate-950 p-7 lg:grid-cols-[1fr_1.4fr_1fr] lg:items-center">
            <div>
              <p className="text-3xl font-black text-white">Truth<span className="text-cyber-cyan">Lens</span> AI</p>
              <p className="mt-2 text-sm font-bold text-slate-300">See Truth. Stop Deception.</p>
            </div>
            <div className="text-center">
              <h1 className="text-3xl font-black uppercase tracking-wide text-white md:text-4xl">Forensic Analysis Report</h1>
              <p className="mt-2 text-xl font-semibold text-cyber-cyan">Unified Cybersecurity Verification</p>
            </div>
            <div className="rounded-2xl border border-cyber-cyan/25 bg-slate-950/60 p-4 text-sm">
              <p><span className="text-slate-500">Report ID:</span> {id.slice(0, 18)}</p>
              <p><span className="text-slate-500">Analysis Date:</span> {new Date(report.uploaded_at).toLocaleString()}</p>
              <p><span className="text-slate-500">Version:</span> 2.0.0</p>
              <p><span className="text-slate-500">Analyzed By:</span> TruthLens AI Engine</p>
            </div>
          </div>
        </section>

        {judgeMode && (
          <div className="mb-6 flex items-center gap-3 rounded-2xl border border-cyber-cyan/30 bg-cyber-cyan/10 px-5 py-4 text-cyber-cyan">
            <Sparkles className="h-5 w-5" />
            <p className="font-black">Cyberathon Judge Mode · Evidence-driven forensic report</p>
          </div>
        )}
        <div className="flex flex-col gap-4 md:flex-row md:items-center md:justify-between">
          <Link href="/" className="inline-flex items-center gap-2 text-sm font-bold text-cyber-cyan">
            <ArrowLeft className="h-4 w-4" />
            New analysis
          </Link>
          <button
            type="button"
            onClick={() => downloadPdf(report)}
            className="inline-flex items-center justify-center gap-2 rounded-2xl bg-cyber-cyan px-5 py-3 font-black text-slate-950"
          >
            <Download className="h-4 w-4" />
            Download PDF report
          </button>
        </div>

        <section className="mt-8 grid gap-6 lg:grid-cols-[1fr_0.8fr]">
          <div className="glass rounded-[2rem] p-7">
            <p className="text-sm font-semibold uppercase tracking-[0.35em] text-cyber-cyan">TruthLens report</p>
            <h1 className="mt-4 break-words text-4xl font-black text-white md:text-5xl">{report.filename}</h1>
            <p className="mt-4 text-slate-300">
              This explainable prototype report combines metadata, visual signals, lip-sync scoring, and audio clone indicators.
            </p>
            <div className="mt-6 flex flex-wrap gap-3">
              <span className="rounded-full border border-cyber-cyan/30 bg-cyber-cyan/10 px-4 py-2 text-sm font-black text-cyber-cyan">
                Detected Media Type: {report.media_type.toUpperCase()}
              </span>
              <span className="rounded-full border border-white/15 bg-white/5 px-4 py-2 text-sm font-black text-white">
                {report.verdict}
              </span>
            </div>
            <div className="mt-6 rounded-2xl border border-cyber-amber/30 bg-cyber-amber/10 p-4 text-cyber-amber">
              <div className="flex items-center gap-3 font-black">
                <AlertTriangle className="h-5 w-5" />
                {report.awareness_message}
              </div>
            </div>
          </div>

          <div className="glass rounded-[2rem] p-7">
            <div className="flex items-center gap-3">
              <ShieldAlert className={`h-9 w-9 ${riskClasses[tone]}`} />
              <div>
                <p className="text-sm uppercase tracking-[0.25em] text-slate-400">Risk level</p>
                <p className={`text-4xl font-black ${riskClasses[tone]}`}>{report.scores.risk_level}</p>
              </div>
            </div>
            <p className="mt-5 text-sm leading-6 text-slate-300">
              Use this result as a triage signal. High-impact public safety, financial, or reputation decisions should involve expert review.
            </p>
          </div>
        </section>

        <section className="mt-6 grid gap-5 md:grid-cols-2 lg:grid-cols-4">
          <MetricCard label="Authenticity" value={`${report.scores.authenticity_score}%`} tone="green" />
          <MetricCard label={report.media_type === "image" ? "AI probability" : "Synthetic probability"} value={`${report.scores.deepfake_probability}%`} tone={tone} />
          <MetricCard label="Threat score" value={`${report.scores.threat_score}%`} tone={tone} />
          <MetricCard label="Confidence" value={`${report.scores.confidence_score}%`} tone="cyan" />
        </section>

        <section className="mt-6 grid gap-6 lg:grid-cols-2">
          <div className="glass rounded-[2rem] p-7">
            <h2 className="text-2xl font-black text-white">Metadata report</h2>
            <div className="mt-5 grid gap-3 text-sm text-slate-300">
              <p><span className="text-slate-500">File size:</span> {report.metadata.file_size_mb} MB</p>
              <p><span className="text-slate-500">Creation date:</span> {new Date(report.metadata.creation_date).toLocaleString()}</p>
              <p><span className="text-slate-500">Codec:</span> {report.metadata.codec}</p>
              <p><span className="text-slate-500">Duration:</span> {report.metadata.duration_seconds ?? "Prototype mode"} seconds</p>
              <p><span className="text-slate-500">Camera:</span> {report.metadata.camera_information}</p>
              <p><span className="text-slate-500">Editing software:</span> {report.metadata.editing_software}</p>
            </div>
            {Object.keys(report.metadata.exif_data).length > 0 && (
              <div className="mt-5 rounded-2xl border border-slate-700 bg-slate-950/50 p-4">
                <p className="font-black text-white">EXIF Data</p>
                <div className="mt-3 grid gap-2 text-xs text-slate-400 sm:grid-cols-2">
                  {Object.entries(report.metadata.exif_data).slice(0, 8).map(([key, value]) => (
                    <p key={key}><span className="text-slate-500">{key}:</span> {value}</p>
                  ))}
                </div>
              </div>
            )}
            <div className="mt-5 space-y-3">
              {report.metadata.tampering_indicators.map((indicator) => (
                <div key={indicator} className="rounded-2xl border border-slate-700 bg-slate-950/50 p-3 text-sm text-slate-300">
                  {indicator}
                </div>
              ))}
            </div>
          </div>

          <div className="glass rounded-[2rem] p-7">
            <h2 className="text-2xl font-black text-white">Highlighted evidence</h2>
            <div className="mt-5 space-y-4">
              {report.evidence.map((item) => (
                <div key={`${item.label}-${item.detail}`} className="rounded-2xl border border-slate-700 bg-slate-950/60 p-4">
                  <div className="flex items-center justify-between gap-3">
                    <h3 className="font-black text-white">{item.label}</h3>
                    <span className="rounded-full border border-cyber-cyan/30 px-3 py-1 text-xs font-bold text-cyber-cyan">{item.severity}</span>
                  </div>
                  <p className="mt-2 text-sm leading-6 text-slate-400">{item.detail}</p>
                </div>
              ))}
            </div>
          </div>
        </section>

        <section className="mt-6 grid gap-6 lg:grid-cols-2">
          <div className="glass rounded-[2rem] p-7">
            <div className="flex items-center gap-3">
              <ScanSearch className="h-6 w-6 text-cyber-cyan" />
              <h2 className="text-2xl font-black text-white">Reasons for decision</h2>
            </div>
            <div className="mt-5 grid gap-3 sm:grid-cols-2">
              {report.reasons_for_decision.map((reason) => (
                <div key={reason} className="flex items-center gap-3 rounded-2xl border border-slate-700 bg-slate-950/60 p-4">
                  <CheckCircle2 className="h-5 w-5 shrink-0 text-cyber-green" />
                  <p className="text-sm font-semibold text-slate-200">{reason}</p>
                </div>
              ))}
            </div>
          </div>
          <div className="glass rounded-[2rem] p-7">
            <h2 className="text-2xl font-black text-white">Recommendations</h2>
            <div className="mt-5 space-y-3">
              {report.recommendations.map((recommendation, index) => (
                <div key={recommendation} className="flex gap-3 rounded-2xl border border-slate-700 bg-slate-950/60 p-4">
                  <span className="font-black text-cyber-cyan">{String(index + 1).padStart(2, "0")}</span>
                  <p className="text-sm leading-6 text-slate-300">{recommendation}</p>
                </div>
              ))}
            </div>
          </div>
        </section>

        {report.media_type === "image" && Object.keys(report.image_forensics).length > 0 && (
          <section className="mt-6 glass rounded-[2rem] p-7">
            <h2 className="text-2xl font-black text-white">Image forensics module</h2>
            <p className="mt-2 text-sm text-slate-400">{String(report.image_forensics.summary ?? "")}</p>
            <div className="mt-6 grid gap-4 sm:grid-cols-2 lg:grid-cols-4">
              {[
                ["Texture inconsistencies", report.image_forensics.texture_inconsistency],
                ["Lighting inconsistencies", report.image_forensics.lighting_inconsistency],
                ["Edge anomalies", report.image_forensics.edge_anomaly],
                ["Face/finger irregularities", report.image_forensics.face_or_finger_irregularity],
              ].map(([label, value]) => (
                <div key={String(label)} className="rounded-2xl border border-slate-700 bg-slate-950/60 p-5">
                  <p className="text-sm text-slate-400">{label}</p>
                  <p className="mt-3 text-3xl font-black text-cyber-cyan">{String(value ?? 0)}%</p>
                </div>
              ))}
            </div>
          </section>
        )}

        {(report.media_type === "url" || report.media_type === "email") && (
          <section className="mt-6 glass rounded-[2rem] p-7">
            <h2 className="text-2xl font-black text-white">{report.media_type === "url" ? "URL risk indicators" : "Email phishing highlights"}</h2>
            <div className="mt-5 grid gap-3 md:grid-cols-2">
              {report.reasons_for_decision.map((reason) => (
                <div key={reason} className="rounded-2xl border border-cyber-red/25 bg-cyber-red/10 p-4 text-sm font-semibold text-red-100">
                  {reason}
                </div>
              ))}
            </div>
          </section>
        )}

        <section className="mt-6 glass rounded-[2rem] p-7">
          <div className="flex items-center gap-3">
            <Eye className="h-6 w-6 text-cyber-cyan" />
            <h2 className="text-2xl font-black text-white">{report.media_type === "image" ? "Suspicious regions & heatmap" : "Suspicious frames"}</h2>
          </div>
          {report.suspicious_frames.length === 0 ? (
            <p className="mt-5 text-slate-400">
              {report.media_type === "audio" ? "Audio spectrogram evidence is summarized in the evidence and score panels." : report.media_type === "url" || report.media_type === "email" ? "Text-based evidence is highlighted in the phishing indicator panel." : "No suspicious visual regions were extracted for this media file."}
            </p>
          ) : (
            <div className="mt-6 grid gap-5 md:grid-cols-2 lg:grid-cols-4">
              {report.suspicious_frames.map((frame) => (
                <div key={`${frame.timestamp_seconds}-${frame.score}`} className="overflow-hidden rounded-2xl border border-slate-700 bg-slate-950/70">
                  {/* eslint-disable-next-line @next/next/no-img-element */}
                  <img src={frameSource(frame.frame_url)} alt={frame.reason} className="h-44 w-full object-cover" />
                  <div className="p-4">
                    <p className="font-black text-white">{frame.score}% anomaly</p>
                    <p className="mt-1 text-xs text-slate-400">{frame.timestamp_seconds}s - {frame.reason}</p>
                  </div>
                </div>
              ))}
            </div>
          )}
        </section>

        <footer className="mt-6 rounded-[2rem] border border-slate-700 bg-slate-950/80 p-6">
          <h2 className="text-lg font-black text-white">Disclaimer</h2>
          <p className="mt-3 text-sm leading-6 text-slate-400">
            This report has been generated using automated forensic analysis techniques. Results are probabilistic assessments and should be considered advisory in nature. TruthLens AI does not guarantee absolute authenticity or inauthenticity. Additional human verification is recommended for critical decisions.
          </p>
          <p className="mt-5 text-center text-sm font-black uppercase tracking-[0.25em] text-cyber-cyan">Generated by TruthLens AI Engine · TEAM TRUTHLENS · See Truth. Stop Deception.</p>
        </footer>
      </div>
    </main>
  );
}
